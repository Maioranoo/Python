dia_da_semana=int(input("digite um numero de 0 a 6:"))

if dia_da_semana==0:
    print("segunda")

elif dia_da_semana==1:
    print("terca")

elif dia_da_semana==2:
    print("quarta")

elif dia_da_semana==3:
    print("quinta")

elif dia_da_semana==4:
    print("sexta")

elif dia_da_semana==5:
    print("sabado")

elif dia_da_semana==6:
    print("domingo")

else:
    print("dia invalido")